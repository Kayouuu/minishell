#include "../inc/minishell.h"

int	main(int argc, char *argv[], char *envp[])
{
	t_list_char	*command;
	char		*cmd;
	t_env		env;

	(void)argc;
	(void)argv;
	env.envp = envp;
	while (1)
	{
		cmd = readline("\033[0;36mminishell> \033[0;37m");
		if (!cmd)
			return (0);
		add_history(cmd);
		command = parsing(cmd);
		free(cmd);
		replace_var_and_quote(&command);
		// split_redirection(&command);
		lstclear_char(&command, free);
	}
	return (1);
}
